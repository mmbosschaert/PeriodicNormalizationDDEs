function normalform(jet, hopf::GenHopf, П„s)

    m = length(П„s)
    ПҶ = repeat(hopf.coords,1,m)
    Оұ = hopf.parameters

    О» = hopf.Пү*im
    О”(О») = jet.О”(ПҶ,Оұ,О»)
    О”вҖІ(О»)= jet.О”вҖІ(ПҶ,Оұ,О»)
    О”вҖІвҖІ(О»)= jet.О”вҖІвҖІ(ПҶ,Оұ,О»)

    _, s, V = svd(О”(О»))
    indxmin = last(findmin(s))
    q = V[:, indxmin]

    _, s, V = svd(transpose(О”(О»)))
    indxmin = last(findmin(s))
    p = V[:, indxmin]

    # normalize
    p /=  transpose(p)*(О”вҖІ(О»)*q)

    # define border inverse of characteristic matrix
    О”бҙөбҙәвұҪ(О»,y) = ([О”(О») q; [p' 0]]\[y; 0])[1:end-1]
    function AбҙөбҙәвұҪ(О»,О·,Оә) 
        Оҫ = О”бҙөбҙәвұҪ(О»,О· + Оә*О”вҖІ(О»)*q)
        Оі = first(transpose(p)*(-О”вҖІ(О»)*Оҫ + 0.5*Оә*О”вҖІвҖІ(О»)*q))
        Оё -> exp(О»*Оё)*(Оҫ + Оі*q - Оә*Оё*q)
    end

    # multi-linear forms at bt point
    Оһ(h) = vcat([h(-П„) for П„ вҲҲ П„s]...)
    B(vвӮҒ,vвӮӮ)  = jet.D2(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ))
    C(vвӮҒ,vвӮӮ,vвӮғ)  = jet.D3(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ),Оһ(vвӮғ))
    D(vвӮҒ,vвӮӮ,vвӮғ,vвӮ„)  = jet.D4(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ),Оһ(vвӮғ),Оһ(vвӮ„))
    E(vвӮҒ,vвӮӮ,vвӮғ,vвӮ„,vвӮ…)  = jet.D5(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ),Оһ(vвӮғ),Оһ(vвӮ„),Оһ(vвӮ…))
    JвӮҒ = jet.D01(ПҶ,Оұ)
    AвӮҒ(vвӮҒ,pвӮҒ)  = jet.D11(ПҶ,Оұ,Оһ(vвӮҒ),pвӮҒ)
    BвӮҒ(vвӮҒ,vвӮӮ,pвӮҒ)  = jet.D21(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ),pвӮҒ)
    CвӮҒ(vвӮҒ,vвӮӮ,vвӮғ,pвӮҒ)  = jet.D31(ПҶ,Оұ,Оһ(vвӮҒ),Оһ(vвӮӮ),Оһ(vвӮғ),pвӮҒ)

    П•(Оё) = exp(О»*Оё)*q
    h2000(Оё) = exp(2.0*О»*Оё)*(О”(2.0*О»)\B(П•,П•))
    h1100(_) = О”(0.0)\B(П•,conjвҲҳП•)

    cвӮҒ = first((1/2)*transpose(p)*(B(conjвҲҳП•,h2000) + 2*B(П•,h1100) + C(П•,П•,conjвҲҳП•)))
    в„“вӮҒ = real(cвӮҒ)/hopf.Пү

    h3000(Оё) = exp(3*О»*Оё)*(О”(3*О»)\(3*B(П•,h2000) + C(П•,П•,П•)))
    h2100 = AбҙөбҙәвұҪ(О»,B(conjвҲҳП•,h2000) + 2*B(П•,h1100) + C(П•,П•,conjвҲҳП•),-2cвӮҒ)

    h2200(_) = О”вҖІ(0.0) \ (2B(conjвҲҳП•,h2100) + 2B(П•,conjвҲҳh2100) + B(conjвҲҳh2000, h2000) + 2B(h1100,h1100) + C(П•,П•,conjвҲҳh2000) + 4C(П•,conjвҲҳП•,h1100) + C(conjвҲҳП•,conjвҲҳП•,h2000) + D(П•,П•,conjвҲҳП•,conjвҲҳП•))

    h3100(Оё) = exp(2*О»*Оё)*(О”(2*О»)\(B(conjвҲҳП•,h3000) + 3B(П•,h2100) + 3B(h1100,h2000) + 3C(П•,conjвҲҳП•,h2000) + 3C(П•,П•,h1100) + D(П•,П•,П•,conjвҲҳП•))) - 6cвӮҒ*(О”(2О»)\(О”вҖІ(2О») - Оё*О”(2О»)))*h2000(Оё)

    cвӮӮ = 1/12*transpose(p)*(2B(conjвҲҳП•,h3100) + 3B(П•,h2200) + B(conjвҲҳh2000,h3000) + 6B(h1100,h2100) + 3B(conjвҲҳh2100,h2000)  +  
        6C(conjвҲҳП•,h2000,h1100) + 6C(П•,conjвҲҳП•,h2100)  + C(conjвҲҳП•,conjвҲҳП•,h3000) + 3C(П•,П•,conjвҲҳh2100) +  3C(П•,h2000,conjвҲҳh2000) + 
        6C(П•,h1100,h1100) + 6D(П•,П•,conjвҲҳП•,h1100) + 3D(П•,conjвҲҳП•,conjвҲҳП•,h2000) +
        D(П•,П•,П•,conjвҲҳh2000) + E(П•,П•,П•,conjвҲҳП•,conjвҲҳП•))
    в„“вӮӮ = real(cвӮӮ)/hopf.Пү

    # parameter-related normal form coefficients
    h0010(_) = О”(0.0)\(JвӮҒ*[1.0; 0.0])
    h0001(_) = О”(0.0)\(JвӮҒ*[0.0; 1.0])

    Оі110 = first(transpose(p)*(AвӮҒ(П•,[1.0; 0.0]) + B(П•,h0010)))
    Оі101 = first(transpose(p)*(AвӮҒ(П•,[0.0; 1.0]) + B(П•,h0001)))

    h1010 = AбҙөбҙәвұҪ(О»,AвӮҒ(П•,[1.0; 0.0]) + B(П•,h0010),-Оі110)
    h1001 = AбҙөбҙәвұҪ(О»,AвӮҒ(П•,[0.0; 1.0]) + B(П•,h0001),-Оі101)

    h2010(Оё) = exp(2О»*Оё)*О”(2О»)\(AвӮҒ(h2000,[1.0; 0.0]) + 2B(П•,h1010) + B(h2000,h0010) + BвӮҒ(П•,П•,[1.0; 0.0]) + C(П•,П•,h0010)) - 2Оі110*О”(2О»)\(О”вҖІ(2О») - Оё*О”(2О»))*h2000(Оё)
    h2001(Оё) = exp(2О»*Оё)*О”(2О»)\(AвӮҒ(h2000,[0.0; 1.0]) + 2B(П•,h1001) + B(h2000,h0001) + BвӮҒ(П•,П•,[0.0; 1.0]) + C(П•,П•,h0001)) - 2Оі101*О”(2О»)\(О”вҖІ(2О») - Оё*О”(2О»))*h2000(Оё)

    h1101(Оё) = О”(0.0)\(AвӮҒ(h1100,[1.0; 0.0]) + 2real(B(conjвҲҳП•,h1010)) + B(h1100,h0010) + BвӮҒ(П•,conjвҲҳП•,[1.0; 0.0]) + C(П•,conjвҲҳП•,h0010)) - 2*real(Оі110)*О”(0.0)\(О”вҖІ(0.0) - Оё*О”(0.0))*h1100(Оё)
    h1110(Оё) = О”(0.0)\(AвӮҒ(h1100,[0.0; 1.0]) + 2real(B(conjвҲҳП•,h1001)) + B(h1100,h0001) + BвӮҒ(П•,conjвҲҳП•,[0.0; 1.0]) + C(П•,conjвҲҳП•,h0001)) - 2*real(Оі101)*О”(0.0)\(О”вҖІ(0.0) - Оё*О”(0.0))*h1100(Оё)

    Оі210 = 0.5*transpose(p) * (AвӮҒ(h2100,[1.0; 0.0]) + B(conjвҲҳП•,h2010) + 2B(П•,h1110) + B(h2100,h0010) + B(h2000,conjвҲҳh1010) + 2B(h1100,h1010) + BвӮҒ(h2000,conjвҲҳП•,[1.0; 0.0]) + 2BвӮҒ(П•,h1100,[1.0; 0.0]) + 2C(П•,conjвҲҳП•,h1010) + C(h2000,conjвҲҳП•,h0010) + C(П•,П•,conjвҲҳh1010) + 2C(П•,h1100,h0010) + CвӮҒ(П•,П•,conjвҲҳП•,[1.0; 0.0]) + D(П•,П•,conjвҲҳП•,h0010)) |> first
    Оі201 = 0.5*transpose(p) * (AвӮҒ(h2100,[0.0; 1.0]) + B(conjвҲҳП•,h2001) + 2B(П•,h1101) + B(h2100,h0001) + B(h2000,conjвҲҳh1001) + 2B(h1100,h1001) + BвӮҒ(h2000,conjвҲҳП•,[0.0; 1.0]) + 2BвӮҒ(П•,h1100,[0.0; 1.0]) + 2C(П•,conjвҲҳП•,h1001) + C(h2000,conjвҲҳП•,h0001) + C(П•,П•,conjвҲҳh1001) + 2C(П•,h1100,h0001) + CвӮҒ(П•,П•,conjвҲҳП•,[0.0; 1.0]) + D(П•,П•,conjвҲҳП•,h0001)) |> first

    # (;Оі110,Оі101,Оі210,Оі201,cвӮҒ,cвӮӮ,в„“вӮҒ,в„“вӮӮ,q,h2000,h1100,h2010,h2001,h1101,h1110,h2100,h2200,h3100,h1010,h1001,h0010,h0001)

    ПүвӮӮ = imag([Оі110 Оі101] * ( real([Оі110 Оі101; Оі210 Оі201])\[0.0; 1.0])) |> first

    @show (;Оі110, Оі101, Оі210, Оі201,)

    nmfm = GenHopfNormalform(
        Оі110,
        Оі101,
        Оі210,
        Оі201,
        cвӮҒ,
        cвӮӮ,
        в„“вӮҒ,
        в„“вӮӮ,
        q,
        h2000,
        h1100,
        h0001,
        ПүвӮӮ)

    hopf = @set hopf.nmfm = nmfm
end

function newtonCotesWeights(n::Int)
    if n ==  2
        return 1.0/6.0*[1.0, 4.0, 1.0]
    end
    if n ==  3
        return [1/8, 3/8, 3/8, 1/8]
    end
    if n ==  4
        return 1.0/90.0*[7.0, 32.0, 12.0, 32.0, 7.0]
    end
    if n == 5
        return [19/288, 25/96, 25/144, 25/144, 25/96, 19/288]
    end
    if n == 6
        return [41/840, 9/35, 9/280, 34/105, 9/280, 9/35, 41/840]
    end
    if n == 7
        return [751/17280, 3577/17280, 49/640, 2989/17280, 2989/17280, 49/640, 3577/17280, 751/17280]
    end
end
